/*
자바스크립트의 "do...while"문은 조건식을 확인하기 전에 "코드를 최소 한 번은 실행"한다는 점이 핵심입니다. 

문제 : 
"업다운 게임" 로직 짜기

1부터 10 사이의 랜덤한 숫자 하나를 "정답"으로 정해둡니다. 
사용자가 이 숫자를 맞출 때까지 계속해서 숫자를 입력받는 로직을 구현하세요.

조건:

- 정답보다 낮은 숫자를 입력하면 "더 큰 수를 입력하세요" 출력.
- 정답보다 높은 숫자를 입력하면 "더 작은 수를 입력하세요" 출력.
- 정답을 맞추면 "축하합니다! 정답입니다." 출력 후 종료.

### 제미나이를 사용해서 공부하고 풀어도 상관없습니다.
*/


let number = 3;  
// 정답 숫자를 3으로 설정

do {
  input = Number(prompt("1~10 숫자를 입력하세요"));  
  // 사용자에게 1~10 사이 숫자를 입력받고
  // prompt의 반환값은 문자열이므로 Number()로 숫자로 변환
} while (input !== number);
// 입력한 숫자가 number(3)과 다르면 반복
// 같으면 반복 종료
