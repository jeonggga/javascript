/*
이번에는 반복문 중에서도 조건이 참인 동안 계속해서 실행되는 "while" 문 문제를 만들어 드릴게요.

"while" 문은 주로 "특정 조건이 만족될 때까지" 반복해야 하는 상황에서 자주 사용됩니다. 이번 문제는 숫자를 하나씩 줄여가는 "카운트다운" 프로그램입니다.


문제: 우주선 발사 카운트다운
변수 "count"에 "5"라는 숫자를 저장하고, "while" 문을 사용하여 숫자를 5부터 1까지 하나씩 줄여가며 출력한 뒤, 마지막에 "발사!"를 출력하는 프로그램을 작성해 보세요.

출력 예시:

5 4 3 2 1 발사!

*/


let n = 5;          // 변수 n을 5로 초기화

while (n > 0) {    // n이 0보다 큰 동안 반복
    console.log(`${n}`); // 현재 n 값 출력
    n--;            // n을 1 감소
}

console.log("발사!"); // 반복문이 끝난 후 실행